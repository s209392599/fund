// 
const calculateDailyReturns = (data) => {
  const returns = [];
  for (let i = 1; i < data.length; i++) {
    const prevValue = parseFloat(data[i - 1].totalNetValue);
    const currValue = parseFloat(data[i].totalNetValue);
    const returnRate = (currValue - prevValue) / prevValue;
    returns.push(returnRate);
  }
  return returns;
};

// 
const calculateStandardDeviation = (data) => {
  const mean = data.reduce((sum, value) => sum + value, 0) / data.length;
  const variance = data.reduce((sum, value) => sum + Math.pow(value - mean, 2), 0) / data.length;
  return Math.sqrt(variance);
};

// 
const calculateAnnualizedVolatility = (dailyReturns) => {
  const dailyStdDev = calculateStandardDeviation(dailyReturns);
  const annualizedVolatility = dailyStdDev * Math.sqrt(252);
  return annualizedVolatility;
};

// 
const calculateMaxDrawdown = (data) => {
  let maxDrawdown = 0;
  let peakValue = parseFloat(data[0].totalNetValue);

  for (let i = 1; i < data.length; i++) {
    const currentValue = parseFloat(data[i].totalNetValue);
    if (currentValue > peakValue) {
      peakValue = currentValue;
    } else {
      const drawdown = (peakValue - currentValue) / peakValue;
      if (drawdown > maxDrawdown) {
        maxDrawdown = drawdown;
      }
    }
  }

  return maxDrawdown;
};

// 
const calculateSharpeRatio = (dailyReturns, riskFreeRate = 0.025) => {
  const annualizedReturn = dailyReturns.reduce((sum, value) => sum + value, 0) * 252;
  const annualizedVolatility = calculateAnnualizedVolatility(dailyReturns);
  const sharpeRatio = (annualizedReturn - riskFreeRate) / annualizedVolatility;
  return sharpeRatio;
};

